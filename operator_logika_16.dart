void main() {
  print('Operator logika');

//   Operator Logika
// ● Operator logika adalah operator untuk dua buah data bool
// ● Hasil dari operator logika adalah bool lagi
// Operator Logika
// Operator Keterangan
// && Dan
// || Atau
// ! Kebalikan

//&& dan
  var a = true && true;
  print(a);
  var b = true && false;
  print(b);
  var c = false && true;
  print(c);
  var d = false && false;
  print(d);

  print('==================');

  // || or

  var abang = true || true;
  print(abang);
  var ayah = true || false;
  print(ayah);
  var ibu = false || true;
  print(ibu);
  var anak = false || false;
  print(anak);

  // !
  print('====================');
  var balikan = !true;

  print(balikan);
}
